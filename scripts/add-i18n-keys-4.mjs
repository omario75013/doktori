// ─── Merge helper ─────────────────────────────────────────────────────────────

function mergeByDotPath(obj, dotPath, additions) {
  const keys = dotPath.split(".");
  let cursor = obj;
  for (let i = 0; i < keys.length - 1; i++) {
    if (!cursor[keys[i]]) cursor[keys[i]] = {};
    cursor = cursor[keys[i]];
  }
  const last = keys[keys.length - 1];
  if (!cursor[last]) cursor[last] = {};
  Object.assign(cursor[last], additions);
}

// ─── Apply ────────────────────────────────────────────────────────────────────

for (const locale of ["fr", "ar"]) {
  const data = load(locale);
  const additions = locale === "fr" ? frAdditions : arAdditions;
  for (const [path, keys] of Object.entries(additions)) {
    mergeByDotPath(data, path, keys);
  }
  save(locale, data);
  console.log(`✓ ${locale}.json updated`);
}
