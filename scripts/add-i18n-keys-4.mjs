import { readFileSync, writeFileSync } from "fs";
import { join } from "path";

const ROOT = join(import.meta.dirname, "..", "apps", "web", "i18n", "messages");

function load(locale) {
  return JSON.parse(readFileSync(join(ROOT, `${locale}.json`), "utf8"));
}
function save(locale, data) {
  writeFileSync(join(ROOT, `${locale}.json`), JSON.stringify(data, null, 2) + "\n");
}

